import random

def ceaser_cipher(work, text, shift):
    work = work.lower()
    text = text.lower()
    
    alphabets = ["a", "b", "c", "d", "e", "f", "g", "h", "i", "j", "k", "l", "m", "n", "o", "p", "q", "r", "s", "t", "u", "v", "w", "x", "y", "z"]# ," ", "a", "b", "c", "d", "e", "f", "g", "h", "i", "j", "k", "l", "m", "n", "o", "p", "q", "r", "s", "t", "u", "v", "w", "x", "y", "z"]
    alphabet = ""
    shift_amount = shift
    letterss=alphabets
    if work == "decode":
        shift_amount*=-1
    for i in list(text):
        if i not in letterss:
            alphabet+=i 
        else:
            index = letterss.index(i)
            num = index+shift_amount
            if num > 25:
                x = random.randint(1, shift)
                letterss*=x
                # alphabets/=x
            letter = alphabets[num]
            alphabet+=letter  
    print(f"Your {work}d message is {alphabet}")
    return(alphabet)
    

